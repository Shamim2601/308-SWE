
public class Numbers {
    private int N1;
    private int N2;
    private String req;
    public Numbers(int n1, int n2, String req) {
        N1 = n1;
        N2 = n2;
        this.req = req;
    }
    public int getN1() {
        return N1;
    }
    public void setN1(int n1) {
        N1 = n1;
    }
    public int getN2() {
        return N2;
    }
    public void setN2(int n2) {
        N2 = n2;
    }
    public String getReq() {
        return req;
    }
    public void setReq(String req) {
        this.req = req;
    }
    
    
}
